-- ============================================================================
-- 
-- 이 마이그레이션은 카테고리 "반다이"를 "라부부"로 변경하고 설명도 업데이트합니다.
-- slug는 "bear"로 유지됩니다.
-- 
-- 변경 사항:
-- - 카테고리 이름: "반다이" → "라부부"
-- - 설명: "다마고치, 배스킨라빈스 등 반다이 상품" → "귀엽고 엉뚱한 몬스터"
-- ============================================================================

-- ----------------------------------------------------------------------------
-- 1. 카테고리 이름 및 설명 업데이트
-- ----------------------------------------------------------------------------

-- "반다이" → "라부부" (이름 및 설명 업데이트)
UPDATE categories 
SET 
  name = '라부부',
  description = '귀엽고 엉뚱한 몬스터',
  updated_at = now()
WHERE name = '반다이' OR slug = 'bear';

-- ----------------------------------------------------------------------------
-- 2. 변경 확인 쿼리
-- ----------------------------------------------------------------------------
-- 업데이트된 카테고리를 확인합니다.
SELECT 
  id,
  name,
  slug,
  description,
  updated_at
FROM categories
WHERE slug = 'bear'
ORDER BY updated_at DESC
LIMIT 1;

